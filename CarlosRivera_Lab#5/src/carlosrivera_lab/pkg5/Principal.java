    private void agregar_restauranteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar_restauranteActionPerformed
        // TODO add your handling code here:
        try {
            nombre = nombre_restaurante.getText();
            direccion = direccion_restaurante.getText();
            nivel_seguridad = Integer.parseInt(nivel_restaurante.getText());
            categoria = (String) categoria_restaurante.getSelectedItem();
            calificacion = (Integer) calificacion_restaurante.getValue();
            restaurantes.add(new Restaurantes(categoria, calificacion, nombre, direccion, nivel_seguridad, new Carretera(), new Carretera()));
            JOptionPane.showMessageDialog(this, "El Restaurante fue agregado");
            DefaultTableModel modelo = (DefaultTableModel) tabla_general.getModel();
            Object[] newrow = {nombre_restaurante.getText(), direccion_restaurante.getText(), categoria_restaurante.getSelectedItem(),
                calificacion_restaurante.getValue()};
            modelo.addRow(newrow);
            tabla_general.setModel(modelo);

            DefaultTreeModel m = (DefaultTreeModel) arbol_general.getModel();
            DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) m.getRoot();
            DefaultMutableTreeNode nodo_lugar;
            nodo_lugar = new DefaultMutableTreeNode(new Lugares(nombre,
                    direccion, nivel_seguridad, new Carretera(), new Carretera()));
            DefaultMutableTreeNode categorias;
            categorias = new DefaultMutableTreeNode(categoria_restaurante.getSelectedItem());
            DefaultMutableTreeNode nombres_restaurante;
            nombres_restaurante = new DefaultMutableTreeNode(this.nombre_restaurante.getText());
            categorias.add(nombres_restaurante);
            nodo_lugar.add(categorias);
            raiz.add(categorias);
            m.reload();
            nombre_restaurante.setText("");
            direccion_restaurante.setText("");
            nivel_restaurante.setText("");
            calificacion_restaurante.setValue(1);
            categoria_restaurante.setSelectedItem(0);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "No se Puedo Agregar");
        }
    }//GEN-LAST:event_agregar_restauranteActionPerformed

